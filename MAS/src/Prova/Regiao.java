package Prova;

public class Regiao {

	private String nome;
	private String tipoClima;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getTipoClima() {
		return tipoClima;
	}
	public void setTipoClima(String tipoClima) {
		this.tipoClima = tipoClima;
	}
	
	
	
	
}
